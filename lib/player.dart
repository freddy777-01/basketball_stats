class Player {
  final int? id;
  final String? firstName;
  final String? lastName;
  final String? position;
  final String? city;
  final String? conference;
  final String? team;

  Player({
    required this.id,
    required this.firstName,
    required this.lastName,
    required this.position,
    required this.city,
    required this.conference,
    required this.team,
  });
}
